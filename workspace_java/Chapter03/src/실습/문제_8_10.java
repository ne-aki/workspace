package 실습;

import java.util.Scanner;

public class 문제_8_10 {
  public static void main(String[] args) {
    //정수를 5개 저장할 수 있는 배열
    int[] arr = new int[5];
    Scanner sc = new Scanner(System.in);
    //입력받은 값을 하나씩 배열의 요소에 저장
    for (int i = 0; i < arr.length; i++) {
      arr[i] = sc.nextInt();
    }
    System.out.println(3);

  }
}
